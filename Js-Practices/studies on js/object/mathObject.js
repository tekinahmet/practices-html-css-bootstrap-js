//Math Object

//Math
console.log(Math);

//Math.PI
console.log(Math.PI); //3.141592653589793

//Math.E
console.log(Math.E); //2.718281828459045

//Math.round
const ares = 7.7;
console.log(Math.round(ares)); //8

//Math.ceil
const bres = 7.2;
console.log(Math.ceil(bres)); //8

//Math.floor
const cres = 7.7;
console.log(Math.floor(cres)); //7

//Math.trunc
const dres = 7.7;
console.log(Math.trunc(dres)); //7

//Math.random
const eres = Math.random();
console.log(eres);
console.log(Math.round(eres * 100)); 

//Math.min
const fres = [1, 2, 3, 4, 5];
console.log(Math.min(fres)); //NaN
console.log(Math.min(...fres)); //1

//Math.max
const gres = [1, 2, 3, 4, 5];
console.log(Math.max(...gres)); //5

//Math.sqrt
const hres = 64;
console.log(Math.sqrt(hres)); //8

//Math.cbrt
const ires = 8;
console.log(Math.cbrt(ires)); //2

//Math.abs
const jres = -5;
console.log(Math.abs(jres)); //5

//Math.pow
const kres = 2;
const lres = 3;
console.log(Math.pow(kres, lres)); //8

